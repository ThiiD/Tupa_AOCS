void receivedFromOBDH(){
	uint8_t receivedDataFromOBDH[5];
	HAL_UART_Receive(&huart4, receivedDataFromOBDH, 5, 100);
	if(receivedDataFromOBDH[0] == 1){
		antennaDrive = receivedDataFromOBDH[1];
		antennaDriver(antennaDrive);
	}
	if(receivedDataFromOBDH[2] == 1){
		attitudeSetpoint = ((uint16_t)receivedDataFromOBDH[3] << 8) | receivedDataFromOBDH[4];
	}

}

void antennaDriver(int signal){
	if(signal == 0){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 250);
	}
	if(signal == 1){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 1500);
	}
}











